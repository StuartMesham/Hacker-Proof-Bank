    private boolean readFile() {
        users = new ArrayList<>();
        try {
            BufferedReader br = new BufferedReader(new FileReader("users.txt"));

            String line = br.readLine();
            while ((line = br.readLine()) != null) {
                String username = line;
                String password = br.readLine();
                users.add(new Credential(username, password));
            }

            br.close();
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Could not read users file", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return false;
        }
    }

    private void login() {
        String username = usernameField.getText();
        String password = new String(passwordField.getPassword());

        if (!readFile()) {
            return;
        }

        for (Credential user : users) {
            if (user.getUsername().equals(username) && user.checkPassword(password)) {
                (new AccountInfo(username, password, this)).setVisible(true);
                dispose();
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Invalid username or password", "Login Failed", JOptionPane.ERROR_MESSAGE);
        invalidAttempt();
    }

